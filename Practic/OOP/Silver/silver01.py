'''
clase vehiculo --> color, rudas
|
clase coche --> velocidad km/h, cilindrada (cc)
'''
class Vehiculo:
	def __init__(self, color, ruedas):
		self.color = color
		self.ruedas = ruedas


class Coche:
	def __init(self, velocidad, cilindrada):
		self.velocidad = velocidad
		self.cilindrada = cilindrada


